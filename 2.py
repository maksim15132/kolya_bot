    user_id = str(chat_id)
    
    if user_id not in user_alphabets or not user_alphabets[user_id]:
        send_message_sync(chat_id, 
            "‚ùå *–°–Ω–∞—á–∞–ª–∞ –Ω–∞—Å—Ç—Ä–æ–π—Ç–µ –∞–ª—Ñ–∞–≤–∏—Ç!*\n"
            "–û—Ç–ø—Ä–∞–≤—å—Ç–µ: /setup"
        )
        return
    
    alphabet = user_alphabets[user_id]
    
    # –§–∏–ª—å—Ç—Ä—É–µ–º —Å–ª–æ–≤–∞ –ø–æ –¥–æ—Å—Ç—É–ø–Ω—ã–º –±—É–∫–≤–∞–º
    available_words = []
    for word in WORDS:
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –≤—Å–µ –±—É–∫–≤—ã —Å–ª–æ–≤–∞ –µ—Å—Ç—å –≤ –∞–ª—Ñ–∞–≤–∏—Ç–µ
        word_ok = True
        for char in word:
            if char not in alphabet:
                word_ok = False
                break
        if word_ok:
            available_words.append(word)
    
    if not available_words:
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º, –∫–∞–∫–∏—Ö –±—É–∫–≤ –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç –¥–ª—è —Å–ª—É—á–∞–π–Ω–æ–≥–æ —Å–ª–æ–≤–∞
        random_word = choice(WORDS)
        missing_letters = []
        for char in random_word:
            if char not in alphabet:
                missing_letters.append(char)
        
        send_message_sync(chat_id, 
            f"‚ùå *–ù–µ–¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –±—É–∫–≤ –≤ –∞–ª—Ñ–∞–≤–∏—Ç–µ!*\n\n"
            f"–ù–∞—Å—Ç—Ä–æ–µ–Ω–æ: {len(alphabet)}/32 –±—É–∫–≤\n"
            f"–î–ª—è —Å–ª–æ–≤–∞ '{random_word}' –Ω–µ —Ö–≤–∞—Ç–∞–µ—Ç:\n"
            f"{', '.join(missing_letters)}\n\n"
            f"‚öôÔ∏è –î–æ–±–∞–≤—å—Ç–µ —ç—Ç–∏ –±—É–∫–≤—ã: /setup"
        )
        return
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª–æ–≤–æ
    word = choice(available_words)
    user_current_word[chat_id] = word
    
    print(f"üì§ –û—Ç–ø—Ä–∞–≤–ª—è—é —Å–ª–æ–≤–æ '{word}' –¥–ª—è {chat_id}")
    
    # –°–æ–±–∏—Ä–∞–µ–º —Å—Ç–∏–∫–µ—Ä—ã –≤ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ –±—É–∫–≤ —Å–ª–æ–≤–∞
    sticker_ids = []
    missing_chars = []
    for char in word:
        if char in alphabet:
            sticker_ids.append(alphabet[char])
        else:
            missing_chars.append(char)
    
    if missing_chars:
        print(f"‚ö†Ô∏è –û—à–∏–±–∫–∞: –±—É–∫–≤—ã {missing_chars} –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ –∞–ª—Ñ–∞–≤–∏—Ç–µ")
